//Matrices en JS
//Declaracion de una matriz
let miNumero = [[],[]];

//Modificamos los valores
//Renglon 0
miNumero [0][0] = 3;
miNumero [0][1] = 10;
//Renglo 1
miNumero [1][0] = 23;
miNumero [1][1] = 99;
miNumero [1][2] = 'Casa';

//Leer los valores
console.log(`Elemento renglon 0 [0][0]: ${miNumero[0][0]}`);
console.log(`Elemento renglon 0 [0][1]: ${miNumero[0][1]}`);
console.log(`Elemento renglon 1 [1][0]: ${miNumero[1][0]}`);
console.log(`Elemento renglon 1 [1][1]: ${miNumero[1][1]}`);
console.log(`Elemento renglon 1 [1][2]: ${miNumero[1][2]}`);
/*Nota: Los renglones, 
van ha tener columnas dependiendo de los elementos que vayamos agregando*/