//Operadores Lógicos 
let a = true;
let b = false;
console.log(a);
console.log(b);
// Operador && and (Y): Regresa verdadero si ambos valores lógicos son verdaderos.
console.log(`${a} && ${b} -> ${a&&b}`);
// Operador || or (O): Regresa verdadero si cualquiera de los operandos es verdadero.
console.log(`${a} || ${b} -> ${a || b}`);
// Operador ! not (No): Invierte el resultado logico, true -> false o false -> true.
console.log(`${a} -> ${!a}`);
